package uk.co.tui.fo.book.view.data;

/**
 * @author prashant.godi
 *
 */
public class WebAnalyticsALTFLTData
{
   /**
    * Holds the date.
    */
   private String date;

   /**
    * Holds the duration.
    */
   private String duration;

   /**
    * Holds the depAir
    */
   private String depAir;

   /**
    * @return the date
    */
   public String getDate()
   {
      return date;
   }

   /**
    * @param date the date to set
    */
   public void setDate(final String date)
   {
      this.date = date;
   }

   /**
    * @return the duration
    */
   public String getDuration()
   {
      return duration;
   }

   /**
    * @param duration the duration to set
    */
   public void setDuration(final String duration)
   {
      this.duration = duration;
   }

   /**
    * @return the depAir
    */
   public String getDepAir()
   {
      return depAir;
   }

   /**
    * @param depAir the depAir to set
    */
   public void setDepAir(final String depAir)
   {
      this.depAir = depAir;
   }

}
