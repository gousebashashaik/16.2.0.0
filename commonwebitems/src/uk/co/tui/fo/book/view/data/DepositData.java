package uk.co.tui.fo.book.view.data;


/**
 * @author sreevidhya.r
 *
 */
public class DepositData
{

    /**
     * Due amount for the deposit
     */
    private String depositDueAmount;

    /**
     * Due date for the amount
     */
    private String depositDueDate;

    /**
     * Due amount for the Low Deposit
     */
    private String lowDepositDueAmount;

    /**
     * Due date for the Low deposit amount
     */
    private String lowDepositDueDate;

    /**
     * @return the depositDueAmount
     */
    public String getDepositDueAmount()
    {
        return depositDueAmount;
    }

    /**
     * @param depositDueAmount
     *           the depositDueAmount to set
     */
    public void setDepositDueAmount(final String depositDueAmount)
    {
        this.depositDueAmount = depositDueAmount;
    }

    /**
     * @return the lowDepositDueAmount
     */
    public String getLowDepositDueAmount()
    {
        return lowDepositDueAmount;
    }

    /**
     * @param lowDepositDueAmount
     *           the lowDepositDueAmount to set
     */
    public void setLowDepositDueAmount(final String lowDepositDueAmount)
    {
        this.lowDepositDueAmount = lowDepositDueAmount;
    }

    /**
     * @return the depositDueDate
     */
    public String getDepositDueDate()
    {
        return depositDueDate;
    }

    /**
     * @param depositDueDate
     *           the depositDueDate to set
     */
    public void setDepositDueDate(final String depositDueDate)
    {
        this.depositDueDate = depositDueDate;
    }

    /**
     * @return the lowDepositDueDate
     */
    public String getLowDepositDueDate()
    {
        return lowDepositDueDate;
    }

    /**
     * @param lowDepositDueDate
     *           the lowDepositDueDate to set
     */
    public void setLowDepositDueDate(final String lowDepositDueDate)
    {
        this.lowDepositDueDate = lowDepositDueDate;
    }




}
