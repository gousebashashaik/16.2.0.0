public class CabinClassViewData
{

    private String cabinCode;

    private String cabinName;

    /**
     * @return the cabinCode
     */
    public String getCabinCode()
    {
        return cabinCode;
    }

    /**
     * @param cabinCode
     *           the cabinCode to set
     */
    public void setCabinCode(final String cabinCode)
    {
        this.cabinCode = cabinCode;
    }

    /**
     * @return the cabinName
     */
    public String getCabinName()
    {
        return cabinName;
    }

    /**
     * @param cabinName
     *           the cabinName to set
     */
    public void setCabinName(final String cabinName)
    {
        this.cabinName = cabinName;
    }



}
