public class PaxFlightExtraMappingViewData
{

    private String mealDescription;

    private String seatDescription;
    private int baggageWeight;

    /**
     * @return the mealDescription
     */
    public String getMealDescription()
    {
        return mealDescription;
    }

    /**
     * @param mealDescription
     *           the mealDescription to set
     */
    public void setMealDescription(final String mealDescription)
    {
        this.mealDescription = mealDescription;
    }

    /**
     * @return the seatDescription
     */
    public String getSeatDescription()
    {
        return seatDescription;
    }

    /**
     * @param seatDescription
     *           the seatDescription to set
     */
    public void setSeatDescription(final String seatDescription)
    {
        this.seatDescription = seatDescription;
    }

    /**
     * @return the baggageWeight
     */
    public int getBaggageWeight()
    {
        return baggageWeight;
    }

    /**
     * @param baggageWeight
     *           the baggageWeight to set
     */
    public void setBaggageWeight(final int baggageWeight)
    {
        this.baggageWeight = baggageWeight;
    }




}
