public class MemoViewData
{
    private String memoCode;

    private String memoDescription;

    /**
     * @return the memoCode
     */
    public String getMemoCode()
    {
        return memoCode;
    }

    /**
     * @param memoCode
     *           the memoCode to set
     */
    public void setMemoCode(final String memoCode)
    {
        this.memoCode = memoCode;
    }

    /**
     * @return the memoDescription
     */
    public String getMemoDescription()
    {
        return memoDescription;
    }

    /**
     * @param memoDescription
     *           the memoDescription to set
     */
    public void setMemoDescription(final String memoDescription)
    {
        this.memoDescription = memoDescription;
    }

}
