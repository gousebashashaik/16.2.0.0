package uk.co.tui.manage.viewdata;

/**
 * @author naresh.gls
 *
 */
public class PaymentHistoryViewData
{

    private String paymentDate;
    private String paymentType;
    private String amountPaid;
    private String amountDue;
    private String cardCharges;
    private String paymentReferenceNumber;
    private String merchantReferenceNumber;
    private String authCode;

    /**
     * @return the paymentDate
     */
    public String getPaymentDate()
    {
        return paymentDate;
    }

    /**
     * @param paymentDate
     *           the paymentDate to set
     */
    public void setPaymentDate(final String paymentDate)
    {
        this.paymentDate = paymentDate;
    }

    /**
     * @return the paymentType
     */
    public String getPaymentType()
    {
        return paymentType;
    }

    /**
     * @param paymentType
     *           the paymentType to set
     */
    public void setPaymentType(final String paymentType)
    {
        this.paymentType = paymentType;
    }

    /**
     * @return the amountPaid
     */
    public String getAmountPaid()
    {
        return amountPaid;
    }

    /**
     * @param amountPaid
     *           the amountPaid to set
     */
    public void setAmountPaid(final String amountPaid)
    {
        this.amountPaid = amountPaid;
    }

    /**
     * @return the amountDue
     */
    public String getAmountDue()
    {
        return amountDue;
    }

    /**
     * @param amountDue
     *           the amountDue to set
     */
    public void setAmountDue(final String amountDue)
    {
        this.amountDue = amountDue;
    }

    /**
     * @return the cardCharges
     */
    public String getCardCharges()
    {
        return cardCharges;
    }

    /**
     * @param cardCharges
     *           the cardCharges to set
     */
    public void setCardCharges(final String cardCharges)
    {
        this.cardCharges = cardCharges;
    }

    /**
     * @return the paymentReferenceNumber
     */
    public String getPaymentReferenceNumber()
    {
        return paymentReferenceNumber;
    }

    /**
     * @param paymentReferenceNumber
     *           the paymentReferenceNumber to set
     */
    public void setPaymentReferenceNumber(final String paymentReferenceNumber)
    {
        this.paymentReferenceNumber = paymentReferenceNumber;
    }

    /**
     * @return the merchantReferenceNumber
     */
    public String getMerchantReferenceNumber()
    {
        return merchantReferenceNumber;
    }

    /**
     * @param merchantReferenceNumber
     *           the merchantReferenceNumber to set
     */
    public void setMerchantReferenceNumber(final String merchantReferenceNumber)
    {
        this.merchantReferenceNumber = merchantReferenceNumber;
    }

    /**
     * @return the authCode
     */
    public String getAuthCode()
    {
        return authCode;
    }

    /**
     * @param authCode
     *           the authCode to set
     */
    public void setAuthCode(final String authCode)
    {
        this.authCode = authCode;
    }


}
