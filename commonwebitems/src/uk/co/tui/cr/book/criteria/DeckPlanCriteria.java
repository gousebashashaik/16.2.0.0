package uk.co.tui.cr.book.criteria;

/**
 * The Class DeckPlanOverlayCriteria.
 *
 * @author ramkishore.p
 */
public class DeckPlanCriteria {

    /** The deck no. */
    private String deckNo;

    /** The ship code. */
    private String shipCode;

    /** The code. */
    private String code;

    /**
     * Gets the deck no.
     *
     * @return the deck no
     */
    public String getDeckNo() {
        return deckNo;
    }

    /**
     * Sets the deck no.
     *
     * @param deckNo
     *            the new deck no
     */
    public void setDeckNo(final String deckNo) {
        this.deckNo = deckNo;
    }

    /**
     * Gets the ship code.
     *
     * @return the ship code
     */
    public String getShipCode() {
        return shipCode;
    }

    /**
     * Sets the ship code.
     *
     * @param shipCode
     *            the new ship code
     */
    public void setShipCode(final String shipCode) {
        this.shipCode = shipCode;
    }

    /**
     * Gets the code.
     *
     * @return the code
     */
    public String getCode() {
        return code;
    }

    /**
     * Sets the code.
     *
     * @param code
     *            the new code
     */
    public void setCode(final String code) {
        this.code = code;
    }

}
