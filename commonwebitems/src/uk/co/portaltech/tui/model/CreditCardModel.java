public class CreditCardModel {

    private String percentageCharge;

    private String capAmount;


    /**
     * @return the percentageCharge
     */
    public String getPercentageCharge() {
        return percentageCharge;
    }

    /**
     * @param percentageCharge
     *            the percentageCharge to set
     */
    public void setPercentageCharge(String percentageCharge) {
        this.percentageCharge = percentageCharge;
    }

    /**
     * @return the capAmount
     */
    public String getCapAmount() {
        return capAmount;
    }

    /**
     * @param capAmount the capAmount to set
     */
    public void setCapAmount(String capAmount) {
        this.capAmount = capAmount;
    }



}
