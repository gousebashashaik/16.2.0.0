package uk.co.portaltech.tui.web.view.data;

/**
 * @author EXTLP1
 *
 */
public class CRdestinationViewData
{
   private String cruiseAreaCode;

   private String cruiseAreaName;

   private String url;

   private String countryNames;

   /**
    * @return the cruiseAreaCode
    */
   public String getCruiseAreaCode()
   {
      return cruiseAreaCode;
   }

   /**
    * @param cruiseAreaCode the cruiseAreaCode to set
    */
   public void setCruiseAreaCode(final String cruiseAreaCode)
   {
      this.cruiseAreaCode = cruiseAreaCode;
   }

   /**
    * @return the cruiseAreaName
    */
   public String getCruiseAreaName()
   {
      return cruiseAreaName;
   }

   /**
    * @param cruiseAreaName the cruiseAreaName to set
    */
   public void setCruiseAreaName(final String cruiseAreaName)
   {
      this.cruiseAreaName = cruiseAreaName;
   }

   /**
    * @return the url
    */
   public String getUrl()
   {
      return url;
   }

   /**
    * @param url the url to set
    */
   public void setUrl(final String url)
   {
      this.url = url;
   }

   /**
    * @return the countryNames
    */
   public String getCountryNames()
   {
      return countryNames;
   }

   /**
    * @param countryNames the countryNames to set
    */
   public void setCountryNames(final String countryNames)
   {
      this.countryNames = countryNames;
   }

}
