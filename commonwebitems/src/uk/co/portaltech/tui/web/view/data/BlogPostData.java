public class BlogPostData {
    private String blogTitle;
    private String blogPostTitle;
    private String blogPostLink;
    private String blogPostDescription;
    private String blogColor;
    private String blogDate;
    private String width;
    private String position;
    /**
     * @return the blogDate
     */
    public String getBlogDate() {
        return blogDate;
    }
    /**
     * @param blogDate the blogDate to set
     */
    public void setBlogDate(final String blogDate) {
        this.blogDate = blogDate;
    }
    /**
     * @return the blogColor
     */
    public String getBlogColor() {
        return blogColor;
    }
    /**
     * @param blogColor the blogColor to set
     */
    public void setBlogColor(final String blogColor) {
        this.blogColor = blogColor;
    }
    /**
     * @return the blogTitle
     */
    public String getBlogTitle() {
        return blogTitle;
    }
    /**
     * @param blogTitle the blogTitle to set
     */
    public void setBlogTitle(final  String blogTitle) {
        this.blogTitle = blogTitle;
    }
    /**
     * @return the blogPostTitle
     */
    public String getBlogPostTitle() {
        return blogPostTitle;
    }
    /**
     * @param blogPostTitle the blogPostTitle to set
     */
    public void setBlogPostTitle(final String blogPostTitle) {
        this.blogPostTitle = blogPostTitle;
    }
    /**
     * @return the blogPostLink
     */
    public String getBlogPostLink() {
        return blogPostLink;
    }
    /**
     * @param blogPostLink the blogPostLink to set
     */
    public void setBlogPostLink(final String blogPostLink) {
        this.blogPostLink = blogPostLink;
    }
    /**
     * @return the blogPostDescription
     */
    public String getBlogPostDescription() {
        return blogPostDescription;
    }
    /**
     * @param blogPostDescription the blogPostDescription to set
     */
    public void setBlogPostDescription(final String blogPostDescription) {
        this.blogPostDescription = blogPostDescription;
    }
    /**
     * @return the width
     */
    public String getWidth() {
        return width;
    }
    /**
     * @param width the width to set
     */
    public void setWidth(String width) {
        this.width = width;
    }
    /**
     * @return the position
     */
    public String getPosition() {
        return position;
    }
    /**
     * @param position the position to set
     */
    public void setPosition(String position) {
        this.position = position;
    }


}
