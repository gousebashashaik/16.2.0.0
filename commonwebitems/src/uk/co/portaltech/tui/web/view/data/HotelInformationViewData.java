package uk.co.portaltech.tui.web.view.data;

/**
 * @author niranjani.r
 *
 */
public class HotelInformationViewData {

    private String roomsUrl;
    private String facilitiesUrl;
    private String accomLocationUrl;
    private String accomThingsToDoUrl;
    /**
     * @return the roomsUrl
     */
    public String getRoomsUrl() {
        return roomsUrl;
    }
    /**
     * @param roomsUrl the roomsUrl to set
     */
    public void setRoomsUrl(final String roomsUrl) {
        this.roomsUrl = roomsUrl;
    }
    /**
     * @return the facilitiesUrl
     */
    public String getFacilitiesUrl() {
        return facilitiesUrl;
    }
    /**
     * @param facilitiesUrl the facilitiesUrl to set
     */
    public void setFacilitiesUrl(final String facilitiesUrl) {
        this.facilitiesUrl = facilitiesUrl;
    }
    /**
     * @return the accomLocationUrl
     */
    public String getAccomLocationUrl() {
        return accomLocationUrl;
    }
    /**
     * @param accomLocationUrl the accomLocationUrl to set
     */
    public void setAccomLocationUrl(final String accomLocationUrl) {
        this.accomLocationUrl = accomLocationUrl;
    }
    /**
     * @return the accomThingsToDoUrl
     */
    public String getAccomThingsToDoUrl() {
        return accomThingsToDoUrl;
    }
    /**
     * @param accomThingsToDoUrl the accomThingsToDoUrl to set
     */
    public void setAccomThingsToDoUrl(final String accomThingsToDoUrl) {
        this.accomThingsToDoUrl = accomThingsToDoUrl;
    }

}
