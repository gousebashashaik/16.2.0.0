public class FlightAvailabilityViewData
{

    private String departureDate;

    private String depAirportName;

    private String depAirportCode;

    private boolean limitedAvailabilityIndicator;

    private String limitedAvailabilityText;

    private String adultPrice;

    private String childPrice;

    private String ppPrice;

    /**
     * @return the departureDate
     */
    public String getDepartureDate() {
        return departureDate;
    }

    /**
     * @param departureDate the departureDate to set
     */
    public void setDepartureDate(String departureDate) {
        this.departureDate = departureDate;
    }

    /**
     * @return the depAirportName
     */
    public String getDepAirportName() {
        return depAirportName;
    }

    /**
     * @param depAirportName
     *            the depAirportName to set
     */
    public void setDepAirportName(String depAirportName) {
        this.depAirportName = depAirportName;
    }

    /**
     * @return the depAirportCode
     */
    public String getDepAirportCode() {
        return depAirportCode;
    }

    /**
     * @param depAirportCode
     *            the depAirportCode to set
     */
    public void setDepAirportCode(String depAirportCode) {
        this.depAirportCode = depAirportCode;
    }

    /**
     * @return the limitedAvailabilityIndicator
     */
    public boolean isLimitedAvailabilityIndicator() {
        return limitedAvailabilityIndicator;
    }

    /**
     * @param limitedAvailabilityIndicator the limitedAvailabilityIndicator to set
     */
    public void setLimitedAvailabilityIndicator(boolean limitedAvailabilityIndicator) {
        this.limitedAvailabilityIndicator = limitedAvailabilityIndicator;
    }

    /**
     * @return the limitedAvailabilityText
     */
    public String getLimitedAvailabilityText() {
        return limitedAvailabilityText;
    }

    /**
     * @param limitedAvailabilityText the limitedAvailabilityText to set
     */
    public void setLimitedAvailabilityText(String limitedAvailabilityText) {
        this.limitedAvailabilityText = limitedAvailabilityText;
    }

    /**
     * @return the adultPrice
     */
    public String getAdultPrice() {
        return adultPrice;
    }

    /**
     * @param adultPrice the adultPrice to set
     */
    public void setAdultPrice(String adultPrice) {
        this.adultPrice = adultPrice;
    }

    /**
     * @return the childPrice
     */
    public String getChildPrice() {
        return childPrice;
    }

    /**
     * @param childPrice the childPrice to set
     */
    public void setChildPrice(String childPrice) {
        this.childPrice = childPrice;
    }

    /**
     * @return the ppPrice
     */
    public String getPpPrice() {
        return ppPrice;
    }

    /**
     * @param ppPrice the ppPrice to set
     */
    public void setPpPrice(String ppPrice) {
        this.ppPrice = ppPrice;
    }




}
