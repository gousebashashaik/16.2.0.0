package uk.co.portaltech.tui.web.view.data;

/**
 * @author vinodkumar.g
 *
 */
public class AccommodationSchemaViewData
{

   private String accommodationName;

   private String accommodationPrice;

   private String accommodationReviewCount;

   private String accommodationRatingValue;

   /**
    * @return the accommodationName
    */
   public String getAccommodationName()
   {
      return accommodationName;
   }

   /**
    * @param accommodationName the accommodationName to set
    */
   public void setAccommodationName(final String accommodationName)
   {
      this.accommodationName = accommodationName;
   }

   /**
    * @return the accommodationPrice
    */
   public String getAccommodationPrice()
   {
      return accommodationPrice;
   }

   /**
    * @param accommodationPrice the accommodationPrice to set
    */
   public void setAccommodationPrice(final String accommodationPrice)
   {
      this.accommodationPrice = accommodationPrice;
   }

   /**
    * @return the accommodationReviewCount
    */
   public String getAccommodationReviewCount()
   {
      return accommodationReviewCount;
   }

   /**
    * @param accommodationReviewCount the accommodationReviewCount to set
    */
   public void setAccommodationReviewCount(final String accommodationReviewCount)
   {
      this.accommodationReviewCount = accommodationReviewCount;
   }

   /**
    * @return the accommodationRatingValue
    */
   public String getAccommodationRatingValue()
   {
      return accommodationRatingValue;
   }

   /**
    * @param accommodationRatingValue the accommodationRatingValue to set
    */
   public void setAccommodationRatingValue(final String accommodationRatingValue)
   {
      this.accommodationRatingValue = accommodationRatingValue;
   }

}
