package uk.co.portaltech.tui.web.view.data;

/**
 * @author kavita.na
 *
 */
public class AdultsEditorialViewData
{
    private String featureTitle;
    private String name;
    private String imageUrl;
    private String description;
    private boolean isSubImage;

    /**
     * @return the featureTitle
     */
    public String getFeatureTitle()
    {
        return featureTitle;
    }

    /**
     * @param featureTitle
     *           the featureTitle to set
     */
    public void setFeatureTitle(final String featureTitle)
    {
        this.featureTitle = featureTitle;
    }

    /**
     * @return the name
     */
    public String getName()
    {
        return name;
    }

    /**
     * @param name
     *           the name to set
     */
    public void setName(final String name)
    {
        this.name = name;
    }

    /**
     * @return the imageUrl
     */
    public String getImageUrl()
    {
        return imageUrl;
    }

    /**
     * @param imageUrl
     *           the imageUrl to set
     */
    public void setImageUrl(final String imageUrl)
    {
        this.imageUrl = imageUrl;
    }

    /**
     * @return the description
     */
    public String getDescription()
    {
        return description;
    }

    /**
     * @param description
     *           the description to set
     */
    public void setDescription(final String description)
    {
        this.description = description;
    }

    /**
     * @return the isSubImage
     */
    public boolean isSubImage()
    {
        return isSubImage;
    }

    /**
     * @param isSubImage
     *           the isSubImage to set
     */
    public void setSubImage(final boolean isSubImage)
    {
        this.isSubImage = isSubImage;
    }

}
