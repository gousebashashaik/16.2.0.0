public class ParallaxHeroViewData {
    private String title;
    private String featureTitle;
    private String featureCopy;
    private String imageUrl;




    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }
    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }
    /**
     * @return the featureTitle
     */
    public String getFeatureTitle() {
        return featureTitle;
    }
    /**
     * @param featureTitle the featureTitle to set
     */
    public void setFeatureTitle(String featureTitle) {
        this.featureTitle = featureTitle;
    }
    /**
     * @return the featureCopy
     */
    public String getFeatureCopy() {
        return featureCopy;
    }
    /**
     * @param featureCopy the featureCopy to set
     */
    public void setFeatureCopy(String featureCopy) {
        this.featureCopy = featureCopy;
    }
    /**
     * @return the imageUrl
     */
    public String getImageUrl() {
        return imageUrl;
    }
    /**
     * @param imageUrl the imageUrl to set
     */
    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

}
